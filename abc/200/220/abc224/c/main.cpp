int main() {
  int n, ans = 0;
  cin >> n;
  vector<int> x(n), y(n);
  for (int i = 0; i < n; i++) {
    cin >> x[i] >> y[i];
  }
  for (int i = 0; i < n; i++) {
    for (int j = i + 1; j < n; j++) {
      for (int k = j + 1; k < n; k++) {
        if ((x[i] - x[j]) * (y[i] - y[k]) - (x[i] - x[k]) * (y[i] - y[j])) {
          ans++;
        }
      }
    }
  }
  cout << ans << endl;
}
